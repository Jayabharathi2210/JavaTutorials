public class DataTypeDemonstration {
    
    public static void main(String[] args) {
        
        int var1 = 12345;
        float var2 = 123.456789f;
        double var3 = 123.45678904567890d;
        char var4 = 'A';
        boolean var5 = true;

        System.out.println("Integer - "+var1);
        System.out.println("Float - "+var2);
        System.out.println("Double - "+var3);
        System.out.println("Character - "+var4);
        System.out.println("Boolean - "+var5);

    }
}
